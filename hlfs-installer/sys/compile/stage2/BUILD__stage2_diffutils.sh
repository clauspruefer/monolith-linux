#!/tools/bin/bash

#>> ------------------------------------------------------
#>> SOURCE FUNCTIONS -------------------------------------

. /tmp/build_functions.sh

#>> ------------------------------------------------------
#>> DEPACK + COMPILE -------------------------------------

depack_src_file "diffutils"

#>> ------------------------------------------------------
#>> REPAIR -----------------------------------------------

sed -i 's:= @mkdir_p@:= /bin/mkdir -p:' po/Makefile.in.in

#>> ------------------------------------------------------
#>> CONFIGURE --------------------------------------------

./configure --prefix=/usr

#>> ------------------------------------------------------
#>> COMPILE ----------------------------------------------

make $MAKEFLAGS

#>> ------------------------------------------------------
#>> INSTALL ----------------------------------------------

make install

